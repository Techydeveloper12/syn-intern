"""Basic Python example script."""

def main():
    print("Hello, world!")

    # Simple arithmetic example
    a = 5
    b = 7
    total = a + b
    print(f"The sum of {a} and {b} is {total}.")

    # Conditional logic
    if total > 10:
        print("The total is greater than 10.")
    else:
        print("The total is 10 or less.")


if __name__ == "__main__":
    main()
